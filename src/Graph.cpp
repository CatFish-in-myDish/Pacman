void Graph::initializeGrid() {
  // 1. Create nodes for PATHS only
  for (int x = 0; x < WIDTH; x++) {
    for (int y = 0; y < HEIGHT; y++) {
      if (MAP[y][x] == 1) { // y is row, x is col
        Location loc(x, y);
        nodes[loc] = new Node(loc);
      }
    }
  }

  // 2. Connect neighbors
  for (auto &pair : nodes) {
    Node *current = pair.second;
    int x = current->getLocation().x;
    int y = current->getLocation().y;

    // Normal neighbors
    addNeighbor(current, x, y - 1); 
    addNeighbor(current, x, y + 1); 
    addNeighbor(current, x - 1, y); 
    addNeighbor(current, x + 1, y);
  }
  
  // 3. Connect Tunnels (Row 14)
  Node* leftTunnel = getNode(0, 14);
  Node* rightTunnel = getNode(WIDTH - 1, 14);
  if (leftTunnel && rightTunnel) {
      leftTunnel->addNeighbor(rightTunnel);
      rightTunnel->addNeighbor(leftTunnel);
  }
}

void Graph::addNeighbor(Node *current, int nx, int ny) {
    // Only connect if neighbor exists (is a Path)
    // Tunnels are handled separately or via explicit wrapping if desired.
    // Here we strictly look for existing nodes.
    Node *neighbor = getNode(nx, ny);
    if (neighbor) {
        current->addNeighbor(neighbor);
    }
}

bool Graph::isWall(int x, int y) {
    if (x < 0 || x >= WIDTH || y < 0 || y >= HEIGHT) return true;
    return MAP[y][x] == 0;
}

Node *Graph::getNode(const Location &loc) {
  auto it = nodes.find(loc);
  if (it != nodes.end()) {
    return it->second;
  }
  return nullptr;
}

Node *Graph::getNode(int x, int y) { return getNode(Location(x, y)); }
